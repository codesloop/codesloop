using std::endl;

/**
  @file csl_rpc/src/stub_header.cc
  @brief implementation of codesloop interface descriptor
 */

namespace csl
{
  namespace rpc
  {
    void stub_header::generate(const iface * ifc) 
    {   
      std::string ls; 
        
      ustr filename( ustr(ifc->get_name().c_str()).trim() + ".hh");
      std::fstream output( filename.c_str(), std::ios_base::out );

      output 
        << "/* THIS FILE IS AUTOMATICALLY GENERATED BY CSRGEN */" << endl          
        << "/*          DO NOT EDIT IT MANUALLY               */" << endl
        << endl
        << "#ifndef __csl_interface_" << ifc->get_name().c_str() << endl
        << "#define __csl_interface_" << ifc->get_name().c_str() << endl 
        << "#ifdef __cplusplus" << endl
        << endl
      ;

      /*---------------------------------------------------------\
      |  Generate includes                                       |
      \---------------------------------------------------------*/
      iface::include_iterator inc_it = ifc->get_includes()->begin() ;

      if ( ifc->get_includes()->size() != 0 )
        output << "/* User defined includes */" << endl;

      while ( inc_it != ifc->get_includes()->end() )
      {
        output << "#include " << *inc_it << endl; 
        inc_it++;
      }
      output << endl;

      // TODO: CSL transport and rpc includes comes here
            
      /*---------------------------------------------------------\
      |  Generate namespace info                                 |
      \---------------------------------------------------------*/
      
      // TODO

      /*---------------------------------------------------------\
      |  Generate function specs                                 |
      \---------------------------------------------------------*/
      iface::function_iterator func_it = ifc->get_functions()->begin();

      while ( func_it != ifc->get_functions()->end() )
      {
        func_it++;
      }
      output << endl;


      // TODO

      /*---------------------------------------------------------\
      |  Cleanup                                                 |
      \---------------------------------------------------------*/
      output
        << endl
        << "#ifndef /* __csl_interface_" << ifc->get_name().c_str() << "*/" 
        << endl
        << "#endif /* __cplusplus /*" << endl
        << "/* EOF */" << endl
      ;

      output.close();
    }
  };
};

